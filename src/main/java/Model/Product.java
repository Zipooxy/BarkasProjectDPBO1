package Model;

/**
 *
 * @author ASUS
 */

public abstract class Product{
    private int id;
    private String nama;
    private double harga;

    public Product(int id, String nama, double harga) {
        this.id = id;
        this.nama = nama;
        this.harga = harga;
    }

    public int getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public double getHarga() {
        return harga;
    }

    public abstract void displayInfo();
}
